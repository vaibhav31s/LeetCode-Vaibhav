class Solution {
    public List<Integer> sequentialDigits(int low, int high) {
        String pattern = "123456789";
        int first = String.valueOf(low).length();
        int second = String.valueOf(high).length();
        List<Integer> answer = new ArrayList<>();
        
        for(int len = first; len <= second; len++) {
// <><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><
            for(int i = 0; i <= 9 - len; i++) {
                String sub = pattern.substring(i, i+len);
                int cur = Integer.parseInt(sub);
                if(cur >= low && cur <= high){
                    answer.add(cur);
                }
            }
        }
        
        
        
        return answer;
    }
}